    private void cargarTabla() {
        DefaultTableModel tabla = new DefaultTableModel() {
            
            //  Filas y columnas no editables
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        //  Establecemos nombres columnas
        String titulos[] = {"Num", "Nombre", "Color", "Raza", "Alergico", "At. esp.", "Duenio", "Celular"};
        tabla.setColumnIdentifiers(titulos);
        
        //  Carga datos desde la DB
        List<Mascota> listaMasco = controlLog.traerMascotas();
        
        //  Recorrer la lista y mostrar elementos en la tabla
        if (listaMasco != null) {
            for (Mascota masco : listaMasco) {
                Object[] objMasco = {masco.getNum_cliente(), masco.getNombre_perro(), masco.getColor(), masco.getRaza(), masco.getAlergico(), masco.getAten_esp(), masco.getDuenio().getNombre_duenio(), masco.getDuenio().getCelular()};
                tabla.addRow(objMasco);
            }
        }
       
       tablaMascotas.setModel(tabla);
        
    }
